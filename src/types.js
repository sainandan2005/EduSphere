// Message interface
/**
 * @typedef {Object} Message
 * @property {string} id - The unique identifier for the message.
 * @property {string} content - The content of the message.
 * @property {'user' | 'assistant'} role - The role of the message sender.
 * @property {Date} timestamp - The timestamp when the message was created.
 */

// YouTubeVideo interface
/**
 * @typedef {Object} YouTubeVideo
 * @property {string} id - The unique identifier for the video.
 * @property {string} title - The title of the video.
 * @property {string} thumbnail - The URL of the video's thumbnail.
 * @property {string} channelTitle - The title of the channel that uploaded the video.
 */